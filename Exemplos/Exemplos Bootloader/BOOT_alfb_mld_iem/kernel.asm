		cmp al, -2
		je end

		cmp al, -1
		je inc_dx

		mov ah, 0xc
		int 10h

		inc cx
		jmp print_pixel

		inc_dx:
			mov cx, word[xz]
			inc dx
			jmp print_pixel

		end:
			ret


